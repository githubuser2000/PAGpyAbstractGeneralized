def part_period_economy(ui: UI, lang: str, state: SimState, cfg: Scenario, periods: int, rng: random.Random) -> None:
    spec = SPECS["periods"]
    begin_section(ui, lang, spec)
    ui.sub(L(lang, "calculation"), bg=60, fg=231)
    sectors = [
        {"en": "food", "de": "Nahrung", "k": 1.0, "demand": 520.0 * cfg.food_s, "supply": 470.0},
        {"en": "housing", "de": "Wohnen", "k": 3.0, "demand": 430.0 * cfg.housing_s, "supply": 360.0},
        {"en": "knowledge", "de": "Wissen", "k": 10.0, "demand": 260.0 * cfg.knowledge_s, "supply": 240.0},
        {"en": "communication", "de": "Kommunikation", "k": 7.0, "demand": 310.0 * cfg.communication_s, "supply": 300.0},
        {"en": "energy", "de": "Energie", "k": stacked_factor([3, 5]), "demand": 380.0 * cfg.energy_s, "supply": 340.0},
    ]
    headers = ["sector", "K", "Q", "S", "state", "AW", "P", "T"] if lang == "en" else ["Sektor", "K", "Q", "S", "Lage", "AW", "P", "T"]
    for pno in range(1, periods + 1):
        print()
        label = ("Period %d" % pno) if lang == "en" else ("Periode %d" % pno)
        ui.sub(label, bg=25, fg=231)
        rows: List[List[object]] = []
        period_aw = 0.0
        period_p = 0.0
        for s in sectors:
            scarcity = clamp(s["demand"] / max(1.0, s["supply"]), 0.55, 3.50)
            q = clamp(rng.gauss(1.02, 0.13), 0.65, 1.42)
            m = jitter(rng, 55.0 + 0.10 * s["demand"], 0.16)
            aw = m * s["k"] * q
            price = aw * scarcity * rng.uniform(0.88, 1.16)
            levy = 0.035 * price
            state.public_fund += levy
            state.taxes += levy
            state.money_supply += max(0.0, price - aw) * 0.006
            period_aw += aw
            period_p += price
            production_push = 0.10 + 0.12 * max(0.0, scarcity - 1.0)
            s["supply"] = max(10.0, s["supply"] * 0.88 + m * production_push + rng.uniform(-8, 12))
            rows.append([s[lang], ui.amount(s["k"], "K", 1), ui.amount(q, "Q"), ui.amount(scarcity, "S"), ui.heat(scarcity, lang=lang), ui.amount(aw, "AW"), ui.amount(price, "P"), ui.amount(levy, "T")])
        emergency = max(0.0, (cfg.public_pressure - 1.0) * 125.0 + (period_p / 8200.0) * 35.0)
        if emergency > state.public_fund:
            created = emergency - state.public_fund
            state.money_supply += created
            state.public_created += created
            state.public_fund = 0.0
        else:
            state.public_fund -= emergency
        state.economy_total_aw += period_aw
        print("  " + ui.c("ledger" if lang == "en" else "Register", fg=229, bold=True) + "  " + ui.unit("GF") + "=" + ui.amount(state.public_fund, "GF") + "  " + ui.unit("GM") + "=" + ui.amount(state.money_supply, "GM"))
        ui.table(headers, rows, right=[1, 2, 3, 5, 6, 7])
        if emergency > 0:
            ui.kv("stabilisation" if lang == "en" else "Stabilisierung", ui.amount(emergency, "AW"))
    result = (
        "Across %d period(s), sectors generated %s. The public fund ends at %s and the circulating money supply is %s. This shows whether the economy is producing enough real work to support its public stabilisation." %
        (periods, ui.strip(ui.amount(state.economy_total_aw, "AW")), ui.strip(ui.amount(state.public_fund, "GF")), ui.strip(ui.amount(state.money_supply, "GM")))
        if lang == "en" else
        "Über %d Periode(n) erzeugten die Sektoren %s. Der Gemeinfonds endet bei %s und die umlaufende Geldmenge liegt bei %s. Das zeigt, ob die Wirtschaft genug reale Arbeit erzeugt, um ihre öffentliche Stabilisierung zu tragen." %
        (periods, ui.strip(ui.amount(state.economy_total_aw, "AW")), ui.strip(ui.amount(state.public_fund, "GF")), ui.strip(ui.amount(state.money_supply, "GM")))
    )
    possible = (
        [
            "Crisis spiral: S remains high, P rises, public stabilisation drains GF, and GM must expand.",
            "Balanced recovery: sector supply catches up, S cools down, T becomes enough to refill GF.",
            "Innovation boom: knowledge or communication with strong K raises AW faster than scarcity raises P.",
        ] if lang == "en" else [
            "Krisenspirale: S bleibt hoch, P steigt, Stabilisierung leert GF, und GM muss wachsen.",
            "Ausgeglichene Erholung: Sektorangebot holt auf, S kühlt ab, T reicht wieder zum Auffüllen von GF.",
            "Innovationsboom: Wissen oder Kommunikation mit starkem K hebt AW schneller, als Knappheit P erhöht.",
        ]
    )
    end_section(ui, lang, result, possible)


