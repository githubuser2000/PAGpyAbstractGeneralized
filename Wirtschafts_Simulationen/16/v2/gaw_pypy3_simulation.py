def parse_parts(value: str) -> List[str]:
    value = (value or "all").strip().lower()
    if value in PART_ALIASES:
        normalized = PART_ALIASES[value]
        if normalized == "all":
            return list(DEFAULT_ORDER)
        if normalized == "short":
            return list(SHORT_ORDER)
    result: List[str] = []
    for raw in value.split(","):
        key = raw.strip().lower()
        if not key:
            continue
        if key not in PART_ALIASES or PART_ALIASES[key] in ("all", "short"):
            allowed = ", ".join(DEFAULT_ORDER)
            raise SystemExit("Unknown part '%s'. Allowed examples: all, short, %s" % (raw, allowed))
        result.append(PART_ALIASES[key])
    if len(result) > 1 and "final" not in result:
        result.append("final")
    return result


