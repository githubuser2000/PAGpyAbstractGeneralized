@dataclass(frozen=True)
class SectionSpec:
    key: str
    title_en: str
    title_de: str
    why_en: str
    why_de: str
    units: Tuple[str, ...]
    palette: Tuple[int, ...]
    art_factory: Callable[[UI, str], Sequence[str]]

    def title(self, lang: str) -> str:
        return self.title_de if lang == "de" else self.title_en

    def why(self, lang: str) -> str:
        return self.why_de if lang == "de" else self.why_en


def explain_units(ui: UI, lang: str, units: Sequence[str]) -> None:
    for key in units:
        prefix = "  " + ui.unit(key) + " " + ui.c("=", fg=244, bold=True) + " "
        plain_prefix_len = len(ui.strip(prefix))
        width = max(50, ui.width - plain_prefix_len - 2)
        wrapped = textwrap.wrap(UNIT_INFO[key][lang], width=width) or [""]
        print(prefix + wrapped[0])
        continuation = " " * plain_prefix_len
        for line in wrapped[1:]:
            print(continuation + line)


def begin_section(ui: UI, lang: str, spec: SectionSpec) -> None:
    ui.title(spec.title(lang), bg=220 if lang == "en" else 214)
    ui.sub(L(lang, "what_why"), bg=54, fg=231)
    ui.wrap(spec.why(lang), indent=2)
    print()
    ui.sub(L(lang, "units_here"), bg=190, fg=16)
    explain_units(ui, lang, spec.units)
    print()
    ui.sub(L(lang, "art"), bg=93, fg=231)
    ui.print_art(spec.art_factory(ui, lang), palette=spec.palette)
    print()


def end_section(ui: UI, lang: str, result_text: str, possible: Sequence[str]) -> None:
    print()
    ui.sub(L(lang, "summary"), bg=220, fg=16)
    ui.wrap(result_text, indent=2)
    print()
    ui.sub(L(lang, "possible"), bg=33, fg=231)
    for i, item in enumerate(possible):
        color = UI.RAINBOW[i % len(UI.RAINBOW)]
        ui.bullet(item, color=color, indent=2)
    print()


