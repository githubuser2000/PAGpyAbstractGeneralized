class UI:
    """ANSI-256 terminal UI. Designed to work under PyPy3 with stdlib only."""

    UNIT_STYLE: Dict[str, Tuple[int, int]] = {
        # unit: (foreground, background)
        "ID": (16, 244),
        "E": (16, 45),
        "3D": (16, 213),
        "O": (16, 82),
        "O+": (16, 118),
        "O-": (231, 196),
        "M": (16, 201),
        "Mwir": (16, 207),
        "K": (231, 33),
        "Q": (231, 129),
        "AW": (16, 220),
        "P": (16, 208),
        "S": (231, 197),
        "ST": (16, 141),
        "α": (16, 39),
        "β": (16, 77),
        "γ": (16, 162),
        "GF": (16, 51),
        "GM": (16, 226),
        "T": (16, 117),
        "R": (16, 166),
        "KR": (16, 214),
        "B": (16, 34),
        "REG": (16, 40),
        "ENT": (16, 202),
        "SCH": (231, 160),
    }

    UNIT_FG: Dict[str, int] = {
        "ID": 250, "E": 45, "3D": 213, "O": 82, "O+": 118, "O-": 196,
        "M": 201, "Mwir": 207, "K": 33, "Q": 129, "AW": 220, "P": 208,
        "S": 197, "ST": 141, "α": 39, "β": 77, "γ": 162,
        "GF": 51, "GM": 226, "T": 117, "R": 166, "KR": 214,
        "B": 34, "REG": 40, "ENT": 202, "SCH": 160,
    }

    RAINBOW = [196, 202, 226, 46, 51, 39, 129, 201, 213, 118]
    FIRE = [196, 202, 208, 214, 220, 226]
    WATER = [20, 27, 33, 39, 45, 51, 87]
    FOREST = [22, 28, 34, 40, 46, 82, 118]
    VIOLET = [54, 91, 93, 129, 165, 201, 207]

    def __init__(self, color: bool = True, width: Optional[int] = None) -> None:
        self.color = bool(color)
        if width is None:
            width = shutil.get_terminal_size((116, 32)).columns
        self.width = max(82, min(150, int(width)))

    def c(self, text: object, fg: Optional[int] = None, bg: Optional[int] = None,
          bold: bool = False, dim: bool = False) -> str:
        text = str(text)
        if not self.color:
            return text
        codes: List[str] = []
        if bold:
            codes.append("1")
        if dim:
            codes.append("2")
        if fg is not None:
            codes.append("38;5;%d" % fg)
        if bg is not None:
            codes.append("48;5;%d" % bg)
        if not codes:
            return text
        return "\033[%sm%s\033[0m" % (";".join(codes), text)

    def strip(self, text: object) -> str:
        return ANSI_RE.sub("", str(text))

    def unit(self, name: str) -> str:
        fg, bg = self.UNIT_STYLE.get(name, (231, 238))
        return self.c(" %s " % name, fg=fg, bg=bg, bold=True)

    def amount(self, value: float, unit: str, digits: int = 2, signed: bool = False) -> str:
        sign = "+" if signed else ""
        spec = sign + ",." + str(digits) + "f"
        number = format(float(value), spec).replace(",", "_")
        return self.c(number, fg=self.UNIT_FG.get(unit, 231), bold=True) + " " + self.unit(unit)

    def rawnum(self, value: float, digits: int = 2, signed: bool = False) -> str:
        sign = "+" if signed else ""
        spec = sign + ",." + str(digits) + "f"
        return format(float(value), spec).replace(",", "_")

    def rainbow(self, text: str, palette: Optional[Sequence[int]] = None, bold: bool = True) -> str:
        if not self.color:
            return text
        colors = list(palette or self.RAINBOW)
        out: List[str] = []
        idx = 0
        for ch in text:
            if ch.isspace():
                out.append(ch)
            else:
                out.append(self.c(ch, fg=colors[idx % len(colors)], bold=bold))
                idx += 1
        return "".join(out)

    def rule(self, char: str = "═", palette: Optional[Sequence[int]] = None) -> None:
        print(self.rainbow(char * self.width, palette=palette or self.RAINBOW))

    def title(self, text: str, bg: int = 220, fg: int = 16) -> None:
        print()
        self.rule("═", self.RAINBOW)
        inner = "  " + text.strip() + "  "
        pad = max(0, self.width - len(inner))
        left = pad // 2
        right = pad - left
        print(self.c(" " * left + inner + " " * right, fg=fg, bg=bg, bold=True))
        self.rule("═", self.RAINBOW)

    def sub(self, text: str, bg: int = 54, fg: int = 231) -> None:
        print(self.c("  " + text + "  ", fg=fg, bg=bg, bold=True))

    def wrap(self, text: str, indent: int = 0) -> None:
        width = max(52, self.width - indent)
        prefix = " " * indent
        for para in text.strip().split("\n"):
            if not para.strip():
                print()
                continue
            print(textwrap.fill(para.strip(), width=width,
                                initial_indent=prefix, subsequent_indent=prefix))

    def kv(self, key: str, value: str) -> None:
        print("  " + self.c(key, fg=229, bold=True) + self.c(" → ", fg=244) + value)

    def bullet(self, text: str, color: int = 220, indent: int = 2) -> None:
        marker = self.c("◆", fg=color, bold=True)
        wrapped = textwrap.wrap(text.strip(), width=max(52, self.width - indent - 4)) or [""]
        print(" " * indent + marker + " " + wrapped[0])
        for line in wrapped[1:]:
            print(" " * (indent + 2) + line)

    def heat(self, value: float, low: float = 0.85, high: float = 2.2, lang: str = "en") -> str:
        labels = {
            "en": ("low", "normal", "high", "critical"),
            "de": ("niedrig", "normal", "hoch", "kritisch"),
        }[lang]
        if value < low:
            return self.c(" %s " % labels[0], fg=16, bg=82, bold=True)
        if value < 1.20:
            return self.c(" %s " % labels[1], fg=16, bg=46, bold=True)
        if value < high:
            return self.c(" %s " % labels[2], fg=16, bg=208, bold=True)
        return self.c(" %s " % labels[3], fg=231, bg=196, bold=True)

    def print_art(self, lines: Sequence[str], palette: Optional[Sequence[int]] = None) -> None:
        palette = palette or self.RAINBOW
        for line in lines:
            print("  " + self.rainbow(line.rstrip(), palette=palette))

    def table(self, headers: Sequence[str], rows: Sequence[Sequence[object]],
              right: Optional[Iterable[int]] = None) -> None:
        right_set = set(right or [])
        data = [[str(cell) for cell in row] for row in rows]
        heads = [self.c(h, fg=16, bg=250, bold=True) for h in headers]
        all_rows = [heads] + data
        widths: List[int] = []
        for col in range(len(headers)):
            width = max(len(self.strip(row[col])) for row in all_rows)
            widths.append(min(max(width, 3), 42))

        def fit(cell: str, width: int) -> str:
            raw = self.strip(cell)
            if len(raw) <= width:
                return cell
            return raw[:max(1, width - 1)] + "…"

        def line(row: Sequence[str], is_header: bool = False) -> str:
            parts: List[str] = []
            for i, cell in enumerate(row):
                cell = fit(cell, widths[i])
                raw_len = len(self.strip(cell))
                pad = widths[i] - raw_len
                if i in right_set and not is_header:
                    parts.append(" " * pad + cell)
                else:
                    parts.append(cell + " " * pad)
            return "  " + self.c("│", fg=245) + (" " + self.c("│", fg=245) + " ").join(parts) + self.c("│", fg=245)

        top = "  " + self.c("┌" + "┬".join("─" * (w + 2) for w in widths) + "┐", fg=245)
        mid = "  " + self.c("├" + "┼".join("─" * (w + 2) for w in widths) + "┤", fg=245)
        bot = "  " + self.c("└" + "┴".join("─" * (w + 2) for w in widths) + "┘", fg=245)
        print(top)
        print(line(heads, True))
        print(mid)
        for row in data:
            print(line(row))
        print(bot)


