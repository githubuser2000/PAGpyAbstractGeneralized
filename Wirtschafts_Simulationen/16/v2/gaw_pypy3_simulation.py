def run(args: argparse.Namespace) -> None:
    lang = args.lang
    color_enabled = (not args.no_color) and (os.environ.get("NO_COLOR") is None)
    ui = UI(color=color_enabled, width=args.width)
    rng = random.Random(args.seed)
    cfg = scenario_config(args.scenario)
    periods = max(1, min(60, int(args.periods)))
    state = SimState(seed=args.seed, scenario=cfg.key)
    parts = parse_parts(args.parts)
    part_banner(ui, lang, state, cfg, periods)
    for part in parts:
        PART_FUNCS[part](ui, lang, state, cfg, periods, rng)
    print()
    ui.rule("█", UI.RAINBOW)
    print(ui.rainbow("  " + L(lang, "end") + "  ", UI.RAINBOW))
    ui.rule("█", UI.RAINBOW)


def main(argv: Optional[Sequence[str]] = None) -> int:
    parser = build_arg_parser()
    args = parser.parse_args(argv)
    try:
        run(args)
    except KeyboardInterrupt:
        print("\nInterrupted." if args.lang == "en" else "\nAbgebrochen.", file=sys.stderr)
        return 130
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
