def normalize_scenario(name: str) -> str:
    key = (name or "normal").strip().lower()
    if key not in SCENARIO_ALIASES:
        allowed = ", ".join(sorted(SCENARIO_ALIASES.keys()))
        raise SystemExit("Unknown scenario '%s'. Allowed: %s" % (name, allowed))
    return SCENARIO_ALIASES[key]


def scenario_config(name: str) -> Scenario:
    key = normalize_scenario(name)
    if key == "crisis":
        return Scenario(key, food_s=2.25, housing_s=1.45, knowledge_s=1.30,
                        communication_s=1.90, energy_s=2.15, public_pressure=1.85,
                        credit_success=0.54, extraction_pressure=1.46)
